create index idx_programs_service_name on programs(service_name);
create index idx_programs_program_name on programs(program_name);
create index idx_programs_start on programs(start);
create index idx_programs_end on programs(end);
create index idx_programs_start_end on programs(start, end);
create index idx_programs_actual_start on programs(actual_start);
create index idx_programs_actual_end on programs(actual_end);
create index idx_programs_actual_start_actual_end on programs(actual_start, actual_end);
create index idx_programs_genre on programs(genre);
create index idx_programs_program_text on programs(program_text);
create index idx_programs_program_detail on programs(program_detail);
create index idx_programs_record_filename on programs(record_filename);
create index idx_programs_future on programs(future);

