PRAGMA journal_mode=WAL;
create table programs( id integer primary key, nid integer, tsid integer, service_id integer, service_name integer, service_name_raw blob, event_id integer, program_name text, program_name_raw blob, start integer, end integer, actual_start integer, actual_end integer, genre text, genre_raw blob, program_text text, program_text_raw blob, program_detail text, program_detail_raw blob, record_filename text, future integer );
create table changelog( id integer primary key, time integer, actual_time integer, nid integer, tsid integer, service_id integer, service_name integer, service_name_raw blob, event_id integer, program_name text, program_name_raw blob, start integer, end integer, genre text, genre_raw blob, program_text text, program_text_raw blob, program_detail text, program_detail_raw blob, record_filename text );

create index idx_programs_nid on programs(nid);
create index idx_programs_tsid on programs(tsid);
